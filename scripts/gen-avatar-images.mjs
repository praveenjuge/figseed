// One-shot helper: downloads a set of placeholder avatar photos and writes
// them as a base64 data module at src/data/avatars.ts. The plugin sandbox has
// no network access (manifest networkAccess.allowedDomains = ["none"]), so we
// bundle the bytes at build time and decode them at runtime with
// figma.base64Decode + figma.createImage.
//
// These bytes back the "Avatar" image paint styles (see
// src/componentsPage/avatarStyles.ts) that the Avatar component references.
//
// Re-run only when you want different faces:
//   node scripts/gen-avatar-images.mjs
//
// Tunables (env):
//   FIGSEED_AVATAR_COUNT  how many avatars to fetch (default 10)
//   FIGSEED_AVATAR_SIZE   square pixel size to request (default 160)

import { writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");
const outPath = resolve(root, "src/data/avatars.ts");

const COUNT = Number(process.env.FIGSEED_AVATAR_COUNT || 10);
const SIZE = Number(process.env.FIGSEED_AVATAR_SIZE || 160);

// pravatar.cc serves royalty-free placeholder avatar photos meant exactly for
// this kind of mock/design-system usage. `img=N` returns a distinct face.
const urlFor = (n) => `https://i.pravatar.cc/${SIZE}?img=${n}`;

// Figma's createImage accepts PNG, JPEG, or GIF. Reject anything else so the
// runtime call doesn't throw inside the sandbox.
const ALLOWED = ["image/png", "image/jpeg", "image/gif"];

async function fetchAvatar(n) {
  const url = urlFor(n);
  const res = await fetch(url, { redirect: "follow" });
  if (!res.ok) {
    throw new Error(
      `download failed: ${res.status} ${res.statusText} (${url})`,
    );
  }
  const mime = (res.headers.get("content-type") || "")
    .split(";", 1)[0]
    .trim()
    .toLowerCase();
  if (!ALLOWED.includes(mime)) {
    throw new Error(
      `unsupported content-type ${mime || "<unknown>"} for ${url}`,
    );
  }
  const buf = Buffer.from(await res.arrayBuffer());
  return { url, mime, base64: buf.toString("base64"), bytes: buf.length };
}

const entries = [];
for (let i = 1; i <= COUNT; i++) {
  // pravatar's `img` index is 1-based; fetch sequentially to stay polite.
  // eslint-disable-next-line no-await-in-loop
  entries.push(await fetchAvatar(i));
}

const literals = entries.map((e) => `  "${e.base64}",`).join("\n");
const sources = entries.map((e, i) => `//   ${i + 1}. ${e.url}`).join("\n");

const ts = `// Auto-generated by scripts/gen-avatar-images.mjs. Do not edit by hand.
//
// ${entries.length} square ${SIZE}px avatar photos fetched at build time and
// embedded as base64. They back the "Avatar" image paint styles created in
// src/componentsPage/avatarStyles.ts. The plugin sandbox has no network access
// — we decode these bytes with figma.base64Decode at runtime.
//
// Sources:
${sources}

export const AVATAR_IMAGES: string[] = [
${literals}
];
`;

writeFileSync(outPath, ts);
const totalBytes = entries.reduce((acc, e) => acc + e.bytes, 0);
process.stderr.write(
  `wrote ${outPath} (${entries.length} images, ${totalBytes} bytes raw)\n`,
);
