// One-shot helper: downloads a small image from picsum.photos and writes it
// as a base64 data module at src/data/blurBackground.ts. The plugin sandbox
// has no network access, so we bundle the bytes at build time and decode
// them at runtime with figma.base64Decode.
//
// Re-run only when you want a different image:
//   node scripts/gen-blur-image.mjs
//
// Override the source by passing a URL or a custom seed:
//   node scripts/gen-blur-image.mjs https://example.com/foo.jpg
//   NIRAM_BLUR_SEED=anything node scripts/gen-blur-image.mjs

import { writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");
const outPath = resolve(root, "src/data/blurBackground.ts");

const SEED = process.env.NIRAM_BLUR_SEED || "niram-blur";
const SIZE = 96;
const DEFAULT_URL = `https://picsum.photos/seed/${SEED}/${SIZE}/${SIZE}`;
const url = process.argv[2] || DEFAULT_URL;

const res = await fetch(url, { redirect: "follow" });
if (!res.ok) {
  throw new Error(`download failed: ${res.status} ${res.statusText} (${url})`);
}
const contentType = res.headers.get("content-type") || "";
const buf = Buffer.from(await res.arrayBuffer());

// Figma's createImage accepts PNG, JPEG, or GIF. picsum returns JPEG by
// default; reject anything else so the runtime call doesn't throw.
const mime = contentType.split(";", 1)[0].trim().toLowerCase();
if (!["image/png", "image/jpeg", "image/gif"].includes(mime)) {
  throw new Error(`unsupported content-type ${mime || "<unknown>"} for ${url}`);
}

const base64 = buf.toString("base64");
const ts = `// Auto-generated by scripts/gen-blur-image.mjs. Do not edit by hand.
//
// ${SIZE}x${SIZE} ${mime} image fetched at build time and embedded as base64.
// Used as the backdrop for the "Blur & backdrop" section so the backdrop
// blur effect has rich color information to dissolve. The plugin sandbox
// has no network access — we decode these bytes with figma.base64Decode at
// runtime.
//
// Source: ${url}

export const BLUR_BG_BASE64 =
  "${base64}";
`;

writeFileSync(outPath, ts);
process.stderr.write(
  `wrote ${outPath} (${buf.length} bytes ${mime}, ${base64.length} chars b64)\n`,
);
